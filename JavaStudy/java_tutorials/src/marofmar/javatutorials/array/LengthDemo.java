package marofmar.javatutorials.array;

public class LengthDemo {

	public static void main(String[] args) {
		String[] classGroup = new String[4];
		classGroup[0] = "Carol";
		System.out.println(classGroup.length);
		classGroup[1] = "Yujin";
		classGroup[2] = "Bryson";
		System.out.println(classGroup.length);
		classGroup[3] = "Alex";

	}

}
